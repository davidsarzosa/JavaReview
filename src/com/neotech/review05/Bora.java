package com.neotech.review05;

public class Bora {

	// NO parameters and NO return type
	void addTwoNumbers() {
		int result = 4 + 2;
	}

	// TWO parameters and NO return type
	void addtwoNumbersThatGive(int a, int b) {
		int result = a + b;
	}

	// No parameters With return type
	int addTwoNumbersAndGiveMeTheResult() {
		int result = 2 + 3;
		return result;
	}

	// TWO parameters and WITH return type
	int addTwoNumbersThatGiveAndGiveMeTheResult(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}

}
